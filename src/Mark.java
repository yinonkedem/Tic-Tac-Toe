/**
 * Represents the possible marks on a game board.
 * The marks include BLANK, X, and O.
 * Used to mark positions on the board for players.
 * @author Yinon Kedem
 */
enum Mark {
    /**
     * Represents a blank position on the board.
     */
    BLANK,

    /**
     * Represents the mark 'X' on the board.
     */
    X,

    /**
     * Represents the mark 'O' on the board.
     */
    O
}
